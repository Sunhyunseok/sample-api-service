DROP TABLE IF EXISTS USER;
CREATE TABLE USER (
  ID int NOT NULL AUTO_INCREMENT PRIMARY KEY,
  NAME varchar(255) NOT NULL ,
  EMAIL varchar(255)NOT NULL ,
  AGE int NOT NULL
);

